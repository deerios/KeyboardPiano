	public static int ParseIdentifierResult(byte[] buf)
	{
		byte b0 = buf[0], b1 = buf[1], b2 = buf[2];

		// A75 family
		if ((b0 == 11 && b1 == 1  && b2 == 1) ||
			(b0 == 11 && b1 == 4  && b2 == 1)) return 75;   // A75
		if (b0 == 11 && b1 == 4  && b2 == 3) return 750;  // A75 Pro
		if (b0 == 11 && b1 == 4  && b2 == 2) return 751;  // A75 ISO (UK/DE/FR)
		if (b0 == 11 && b1 == 4  && b2 == 4) return 756;  // A75 Ultra
		if (b0 == 11 && b1 == 5  && b2 == 4) return 757;  // A75 Master

		// G75 family
		if (b0 == 11 && b1 == 4  && b2 == 5) return 754;  // G75
		if (b0 == 11 && b1 == 4  && b2 == 7) return 755;  // G75 JP

		// G65 family
		if ((b0 == 11 && b1 == 2  && b2 == 1) ||
			(b0 == 15 && b1 == 1  && b2 == 1)) return 65;   // G65
		if (b0 == 11 && b1 == 2  && b2 == 3) return 651;  // G65 m1
		if (b0 == 11 && b1 == 16 && b2 == 1) return 652;  // G65 m2
		if (b0 == 11 && b1 == 18 && b2 == 1) return 653;  // G65 m3
		if (b0 == 11 && b1 == 2  && b2 == 5) return 654;  // G65 Lite

		// G60 family
		if (b0 == 11 && b1 == 3  && b2 == 1) return 60;   // G60
		if (b0 == 11 && b1 == 3  && b2 == 3) return 601;  // G60 m1
		if (b0 == 11 && b1 == 19 && b2 == 1) return 602;  // G60 m2
		if (b0 == 11 && b1 == 21 && b2 == 1) return 603;  // G60 m3

		// X60
		if (b0 == 11 && b1 == 6  && b2 == 5) return 640;  // X60

		return 0;
	}

